const PANEL_W = 880
const PANEL_H = 150
const MARGIN = 14

function AirfoilPanel({ fit }: { fit: Fit }) {
  const cps = fit.cpX.map((x, i) => ({ x, y: fit.cpY[i] }))
  // bounds (chord is [0,1]); true-aspect scale to fit the panel width.
  let ymin = Infinity, ymax = -Infinity
  for (const y of fit.dataY) { if (y < ymin) ymin = y; if (y > ymax) ymax = y }
  const scale = (PANEL_W - 2 * MARGIN) / 1.0
  const cy0 = PANEL_H / 2 + ((ymin + ymax) / 2) * scale // vertical centering
  const map = (x: number, y: number) => ({ sx: MARGIN + x * scale, sy: cy0 - y * scale })

  // sample the fitted open curve
  const SAMPLES = 400
  let path = ''
  for (let i = 0; i <= SAMPLES; i++) {
    const p = evaluate(plainCoeffs, cps, fit.degree, fit.knots, i / SAMPLES)
    const { sx, sy } = map(p.x, p.y)
    path += `${i === 0 ? 'M' : 'L'}${sx.toFixed(1)},${sy.toFixed(1)} `
  }
  const extremaPts = fit.extremaT.map((t) => { const p = evaluate(plainCoeffs, cps, fit.degree, fit.knots, t); return map(p.x, p.y) })

  return (
    <div style={{ display: 'flex', alignItems: 'center', gap: 18 }}>
      <svg width={PANEL_W} height={PANEL_H} viewBox={`0 0 ${PANEL_W} ${PANEL_H}`} style={{ flexShrink: 0 }}>
        {/* data points (faint) */}
        {fit.dataX.map((x, i) => { const { sx, sy } = map(x, fit.dataY[i]); return <circle key={i} cx={sx} cy={sy} r={2.1} fill="#64748b" opacity={0.55} /> })}
        {/* fitted open B-spline */}
        <path d={path} fill="none" stroke="#60a5fa" strokeWidth={2.4} strokeLinecap="round" />
        {/* curvature-extrema markers */}
        {extremaPts.map((p, i) => (
          <circle key={i} cx={p.sx} cy={p.sy} r={6.5} fill="none" stroke="#f59e0b" strokeWidth={2.6} />
        ))}
      </svg>
      <div style={{ minWidth: 170 }}>
        <div style={{ fontSize: '0.95em', fontWeight: 600, color: '#e2e8f0' }}>{fit.label}</div>
        <div style={{ fontSize: '0.85em', color: '#f59e0b', marginTop: 2 }}>
          {fit.K} curvature {fit.K === 1 ? 'extremum' : 'extrema'}
        </div>
        <div style={{ fontSize: '0.85em', color: '#34d399', marginTop: 2 }}>
          error {(fit.H * 100).toFixed(3)}% chord
        </div>
      </div>
    </div>
  )
}

